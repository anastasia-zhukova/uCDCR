        mentions_df = pd.read_parquet(os.path.join(dataset_folder, MENTIONS_ALL_PARQUET), engine="pyarrow")
        # double check that the singletons were removed
        chain_df = pd.DataFrame(columns=[SPLIT, COREF_CHAIN, TOPIC_ID, SUBTOPIC_ID, MENTIONS])

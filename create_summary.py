def create_doc_tsne_scatterplot(dataset_dict: dict, max_docs_dataset: int = 100):
    """
    Collects data and plots a scatterplot of tSNE representation of mentions' text.
        The original code belongs to https://gist.github.com/jpwahle/f9414b2ae85043225b10cd9e105d3b81
    # Copyright 2022 by Jan Philip Wahle, https://jpwahle.com/
    # All rights reserved.
    Args:
        dataset_dict: datasets to plot and the folders where the data located
    """
    df_list, dataset_name_list = [], []
    for dataset_name, folder_path in tqdm(dataset_dict.items(), desc="Reading data"):
        df = pd.read_parquet(os.path.join(folder_path, DOCUMENTS_ALL_PARQUET), engine="pyarrow")
        df_list.append(df)
        dataset_name_list.append(dataset_name.replace("-prep", ""))

    X, y = [], []
    stop_words = set(stopwords.words("english"))
    df_path = os.path.join(os.getcwd(), SUMMARY_FOLDER, "scatterplot_doc.csv")

    if not os.path.exists(df_path):

        for dataset_name, dataset_df in tqdm(zip(dataset_name_list, df_list),
                                             desc="Processing documents for tSNE", total=len(dataset_name_list)):
            selected_doc_ids = random.choices(dataset_df[TOPIC_SUBTOPIC_DOC].unique().tolist(), k=max_docs_dataset)
            selected_df = dataset_df[dataset_df[TOPIC_SUBTOPIC_DOC].isin(selected_doc_ids)]

            for topic_subtopic_doc, doc_df in selected_df.groupby(by=[TOPIC_SUBTOPIC_DOC]):
                y.append(dataset_name)
                tokens = []
                for t in doc_df[TOKEN].values:
                    if t in punctuation or t in stop_words:
                        continue
                    tokens.append(t)
                doc_text = " ".join(tokens)
                X.append(doc_text)

        vect = dask_ml.feature_extraction.text.HashingVectorizer()
        X_tfidf = vect.fit_transform(X)

        svd = TruncatedSVD(n_components=160, n_iter=9, random_state=42)
        X_svd = svd.fit_transform(X_tfidf)

        tsne = TSNE(
            perplexity=30,
            metric="euclidean",
            n_jobs=8,
            random_state=42,
            verbose=True,
        )

        LOGGER.info("Plotting documents")
        X_tsne = tsne.fit(X_svd)

        df = pd.DataFrame(X_tsne, columns=["x", "y"])
        df = df.merge(pd.DataFrame({"color": y}, dtype="category"), left_index=True, right_index=True)
        df.to_csv(df_path)
    else:
        df = pd.read_csv(df_path)

    # Create larger figure
    plt.figure(figsize=(12, 8))

    for dataset, group in df.groupby("color"):
        plt.scatter(
            group['x'].values,
            group['y'].values,
            label=CORRECTED_LABELS[dataset],
            color=COLORS.get(dataset, "#000000"),
            s=30
        )

    plt.xlabel("X")
    plt.ylabel("Y")

    # plt.title("tSNE representation of the CDCR mentions")

    # Larger legend markers
    plt.legend(markerscale=2, fontsize=13)

    # Save as high-resolution PNG (300 DPI)
    output_path = os.path.join(os.getcwd(), SUMMARY_FOLDER, "scatterplot_doc.png")
    plt.savefig(output_path, dpi=300)
    plt.close()

    LOGGER.info(f"Scatterplot of documents is saved to {output_path}")



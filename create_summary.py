def mention_stat_analysis(dataset_dict: dict):
    """
    Aggregates basic stats on the POS, NER, and mention types.
    Args:
        dataset_dict: a dict of dataset names and a path to the dataset
    """
    pos_df = pd.DataFrame()
    ner_df = pd.DataFrame()
    type_df = pd.DataFrame()
    now_ = datetime.now()

    for dataset_name_orig, dataset_folder in dataset_dict.items():
        dataset_name = dataset_name_orig.split("-")[0]
        mentions_df = pd.read_parquet(os.path.join(dataset_folder, MENTIONS_ALL_PARQUET), engine="pyarrow")
        pos_local_df =  mentions_df[[MENTION_HEAD_POS, MENTION_ID]].groupby([MENTION_HEAD_POS]).count().T
        pos_local_df.index = [dataset_name]
        pos_df = pd.concat([pos_df, pos_local_df])

        mentions_df[TYPE] = [v.split("_")[0] for v in mentions_df[MENTION_FULL_TYPE].values]
        type_local_df = mentions_df[[TYPE, MENTION_ID]].groupby([TYPE]).count().T
        type_local_df.index = [dataset_name]
        type_df = pd.concat([type_df, type_local_df])

        ner_local_df = mentions_df[[MENTION_NER, MENTION_ID]].groupby([MENTION_NER]).count().T
        ner_local_df.index = [dataset_name]
        ner_df = pd.concat([ner_df, ner_local_df])

    pos_df.fillna(0, inplace=True)
    ner_df.fillna(0, inplace=True)
    type_df.fillna(0, inplace=True)

    output_path = os.path.join(os.getcwd(), SUMMARY_FOLDER)
    pos_df.to_csv(os.path.join(output_path, f'{now_.strftime("%Y-%m-%d_%H-%M-%S")}_pos.csv'))
    ner_df.to_csv(os.path.join(output_path, f'{now_.strftime("%Y-%m-%d_%H-%M-%S")}_ner.csv'))
    type_df.to_csv(os.path.join(output_path, f'{now_.strftime("%Y-%m-%d_%H-%M-%S")}_type.csv'))
    LOGGER.info(f"Stats about POS, NER, and mentions types are saved to  {output_path}")



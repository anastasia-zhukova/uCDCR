    # Filter rows where split == ""
    df_filtered = df[df["split"].str.len() == 0]

    for measure_name in [PHRASING_DIVERSITY, MLTD]:
        # df_filtered = df[df["split"] == "test"]
        max_val = math.floor(df_filtered[measure_name].max())//2
        min_val = math.floor(df_filtered[measure_name].min())
        # max_val = max_val if (max_val - min_val) % bins_num == 0 else ((max_val - min_val) // bins_num + 1) * bins_num
        bins = np.linspace(min_val, max_val, bins_num + 1)

        hist_df = pd.DataFrame()
        for dataset_name, group in df_filtered.groupby("dataset"):
            # bins = np.linspace(all_values.min(), all_values.max(), 31)
            bin_indices = np.digitize(group[measure_name].values, bins)
            bin_dict = Counter(bin_indices)
            hist_df = pd.concat([hist_df, pd.DataFrame(bin_dict, index= [dataset_name]).T], axis=1)
        hist_df.fillna(0, inplace=True)
        hist_df = hist_df.sort_index()
        for i, bin in enumerate(bins):
            if i == 0:
                continue
            if i not in hist_df.index:
                hist_df = pd.concat([hist_df, pd.DataFrame(np.zeros(len(hist_df.columns)).reshape(1,-1), index=[i], columns = hist_df.columns)])
        hist_df = hist_df.sort_index()
        indexes = [f"[{bins[i-1]}..{bins[i]})"for i in range(1, len(bins))]
        indexes.append(f"[{bins[-1]}..")
        hist_df.index = indexes
        hist_df.to_csv(os.path.join(os.getcwd(), SUMMARY_FOLDER, f"histogram_{measure_name}.csv"))
